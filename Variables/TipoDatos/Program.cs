using System;

namespace TipoDatos
{
    class Program
    {
        static void Main(string[] args)
        {
            //enteros
            int numero = 1;
            Console.WriteLine("=======**TIPOS DE DATOS **========");
            Console.WriteLine("Entero: "+numero);
            //entero
            double pi = 3.14;
            Console.WriteLine("Decimal: "+pi);
            // un solo caracter
            char unaCadena = '?';
            Console.WriteLine("Una caracter: "+unaCadena);
            //cadena o string 
            string cadena = "Cadena completas";
            Console.WriteLine("Cadena: "+cadena);
            // boolenado
            bool estado = false;
            Console.WriteLine("Boolenado bool: "+estado);
        }
    }
}
